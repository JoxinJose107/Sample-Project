

# *****WORKING WITH NUMBERS******
# Numbers are the most common datatypes used in python.
# To import special math functions in python.
from math import *
# To print a number
print(2)

# To print a decimal number
print(2.5678)

# To print a negative number
print(-1)


#Basic arithmetic operations
# Addition
print(2+3)

#Substraction
print(2-1)

# Multiplication
print(2*3)

# Division
print(3/2)
print(3//2)

# % modulus returns the reminder
print(10%2)

# abs() function gives the absolute value of the number
value=-5
print("The absolute value is: "+str(abs(value)))

# power() function is used for the exponential purpose. It takes two parameters: 1) number 2) power.
print(pow(3,2))
print(pow(abs(value),2))

# max() function returns the maximum number.
print("The maximum value is: "+str(max(2,4)))

# min() function returns the  minimum value
print("The minimum value is: "+str(min(2,4)))

# round() function rounds the number
print(round(3.2))

# floor() function grabs the lowest number
print(floor(3.7))